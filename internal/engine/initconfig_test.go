func TestRunInit_CreatesTerrainDir(t *testing.T) {
	t.Parallel()
	root := t.TempDir()

	// Create a minimal repo structure.
	writeArtifact(t, root, "src/app.ts", "export function hello() {}")
	writeArtifact(t, root, "test/app.test.ts", "describe('app', () => { it('works', () => {}); });")

	result, err := RunInit(root)
	if err != nil {
		t.Fatalf("RunInit failed: %v", err)
	}

	// .terrain/ directory should be created.
	terrainDir := filepath.Join(root, ".terrain")
	info, err := os.Stat(terrainDir)
	if err != nil {
		t.Fatalf(".terrain/ directory not created: %v", err)
	}
	if !info.IsDir() {
		t.Error(".terrain should be a directory")
	}

	// Policy file should be created.
	if result.PolicyPath == "" {
		t.Error("expected PolicyPath to be set")
	}
	if _, err := os.Stat(result.PolicyPath); err != nil {
		t.Errorf("policy file not created at %s: %v", result.PolicyPath, err)
	}
}

func TestRunInit_GeneratesPolicyYAML(t *testing.T) {
	t.Parallel()
	root := t.TempDir()
	writeArtifact(t, root, "src/app.go", "package main\nfunc main() {}")

	result, err := RunInit(root)
	if err != nil {
		t.Fatalf("RunInit failed: %v", err)
	}

	data, err := os.ReadFile(result.PolicyPath)
	if err != nil {
		t.Fatalf("read policy: %v", err)
	}

	content := string(data)
	// Should contain commented-out rules.
	if !strings.Contains(content, "# disallow_skipped_tests") {
		t.Error("policy should contain commented disallow_skipped_tests rule")
	}
	if !strings.Contains(content, "# minimum_coverage_percent") {
		t.Error("policy should contain commented minimum_coverage_percent rule")
	}
	// Should not have any uncommented rules.
	for _, line := range strings.Split(content, "\n") {
		trimmed := strings.TrimSpace(line)
		if trimmed == "" || strings.HasPrefix(trimmed, "#") || trimmed == "rules:" {
			continue
		}
		t.Errorf("unexpected uncommented line in policy: %q", trimmed)
	}
}


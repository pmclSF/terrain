func TestRunInit_SkipsExistingConfig(t *testing.T) {
	t.Parallel()
	root := t.TempDir()
	writeArtifact(t, root, ".terrain/policy.yaml", "rules:\n  # existing\n")
	writeArtifact(t, root, ".terrain/terrain.yaml", "# existing config\n")
	writeArtifact(t, root, "test/a.test.ts", "it('works', () => {});")

	result, err := RunInit(root)
	if err != nil {
		t.Fatalf("RunInit failed: %v", err)
	}

	// Should not overwrite existing files.
	if result.PolicyPath != "" {
		t.Error("should not generate policy.yaml when it already exists")
	}
	if result.ConfigPath != "" {
		t.Error("should not generate terrain.yaml when it already exists")
	}
	if !result.HasPolicyFile {
		t.Error("HasPolicyFile should be true")
	}
	if !result.HasTerrainYAML {
		t.Error("HasTerrainYAML should be true")
	}

	// Verify existing files are unchanged.
	data, _ := os.ReadFile(filepath.Join(root, ".terrain", "policy.yaml"))
	if string(data) != "rules:\n  # existing\n" {
		t.Error("existing policy.yaml should not be modified")
	}
}


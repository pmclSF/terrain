func TestRunInit_Deterministic(t *testing.T) {
	t.Parallel()
	root := t.TempDir()
	writeArtifact(t, root, "test/a.test.ts", "it('works', () => {});")
	writeArtifact(t, root, "coverage/lcov.info", "TN:\nend_of_record\n")

	r1, err := RunInit(root)
	if err != nil {
		t.Fatalf("first run: %v", err)
	}

	// Delete generated files, run again.
	os.Remove(r1.PolicyPath)
	os.Remove(r1.ConfigPath)

	r2, err := RunInit(root)
	if err != nil {
		t.Fatalf("second run: %v", err)
	}

	// Generated config should be identical.
	d1, _ := os.ReadFile(r2.PolicyPath)
	d2, _ := os.ReadFile(r2.PolicyPath)
	if string(d1) != string(d2) {
		t.Error("policy.yaml should be deterministic")
	}

	if r1.TestFileCount != r2.TestFileCount {
		t.Errorf("test file count not deterministic: %d vs %d", r1.TestFileCount, r2.TestFileCount)
	}
}


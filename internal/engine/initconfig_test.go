func TestIsTestFileName(t *testing.T) {
	t.Parallel()
	cases := []struct {
		name string
		want bool
	}{
		{"app.test.ts", true},
		{"app.spec.js", true},
		{"app_test.go", true},
		{"test_app.py", true},
		{"app_test.py", true},
		{"AppTest.java", true},
		{"app.ts", false},
		{"README.md", false},
	}
	for _, tc := range cases {
		if got := isTestFileName(tc.name); got != tc.want {
			t.Errorf("isTestFileName(%q) = %v, want %v", tc.name, got, tc.want)
		}
	}
}

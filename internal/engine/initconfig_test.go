func TestRunInit_DetectsFrameworks(t *testing.T) {
	t.Parallel()
	root := t.TempDir()
	writeArtifact(t, root, "package.json", `{"devDependencies":{"jest":"^29.0.0","vitest":"^1.0.0"}}`)
	writeArtifact(t, root, "test/a.test.ts", "it('works', () => {});")

	result, err := RunInit(root)
	if err != nil {
		t.Fatalf("RunInit failed: %v", err)
	}

	if len(result.Frameworks) == 0 {
		t.Error("expected at least 1 framework detected")
	}

	names := map[string]bool{}
	for _, fw := range result.Frameworks {
		names[fw.Name] = true
	}
	if !names["jest"] {
		t.Error("expected jest to be detected")
	}
}

func TestRunInit_DetectsArtifacts(t *testing.T) {
	t.Parallel()
	root := t.TempDir()
	writeArtifact(t, root, "coverage/lcov.info", "TN:\nSF:src/a.ts\nend_of_record\n")
	writeArtifact(t, root, "junit.xml", `<?xml version="1.0"?><testsuites/>`)
	writeArtifact(t, root, "test/a.test.ts", "it('works', () => {});")

	result, err := RunInit(root)
	if err != nil {
		t.Fatalf("RunInit failed: %v", err)
	}

	if result.Artifacts.CoveragePath == "" {
		t.Error("expected coverage to be detected")
	}
	if len(result.Artifacts.RuntimePaths) == 0 {
		t.Error("expected runtime to be detected")
	}

	// Config should mention detected artifacts.
	if result.ConfigPath != "" {
		data, _ := os.ReadFile(result.ConfigPath)
		if !strings.Contains(string(data), "Detected coverage") {
			t.Error("config should mention detected coverage in comments")
		}
	}
}


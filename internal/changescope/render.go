// formatSuitePercent renders selected/total as a percentage with
// honest precision for small fractions. Pre-fix the integer-division
// formula floored 7-of-796 to "0% of suite" — adopters seeing
// "0%" reasonably wonder whether selection ran at all. Now sub-1%
// fractions render as "<1%", and values ≥1% render as integer
// percentages.
func formatSuitePercent(selected, total int) string {
	if total <= 0 {
		return "—"
	}
	if selected <= 0 {
		return "0%"
	}
	pct := 100 * selected / total
	if pct == 0 {
		return "<1%"
	}
	return fmt.Sprintf("%d%%", pct)
}

func formatSeverityCounts(counts map[string]int) []string {
	var parts []string
	for _, sev := range []string{"high", "medium", "low"} {
		if c, ok := counts[sev]; ok && c > 0 {
			parts = append(parts, fmt.Sprintf("%d %s", c, sev))
		}
	}
	return parts
}

func deduplicateReasons(reasons []string) string {
	seen := map[string]int{}
	var unique []string
	for _, r := range reasons {
		seen[r]++
		if seen[r] == 1 {
			unique = append(unique, r)
		}
	}
	var parts []string
	for _, r := range unique {
		if seen[r] > 1 {
			parts = append(parts, fmt.Sprintf("%s (%dx)", r, seen[r]))
		} else {
			parts = append(parts, r)
		}
	}
	return strings.Join(parts, "; ")
}

func formatTestReasons(selections []TestSelection) map[string]string {
	reasons := map[string]string{}
	unitTestCount := map[string]int{}
	for _, t := range selections {
		for _, uid := range t.CoversUnits {
			unitTestCount[uid]++
		}
	}
	totalTests := len(selections)
	for _, t := range selections {
		reasons[t.Path] = formatSingleTestReason(t, unitTestCount, totalTests)
	}
	return reasons
}

func formatSingleTestReason(t TestSelection, unitTestCount map[string]int, totalTests int) string {
	unitNames := extractUnitNames(t.CoversUnits)
	if len(unitNames) == 0 {
		if len(t.Reasons) > 0 {
			return deduplicateReasons(t.Reasons)
		}
		return t.Relevance
	}
	var unique, shared []string
	for _, uid := range t.CoversUnits {
		name := uid
		if idx := strings.LastIndex(uid, ":"); idx >= 0 {
			name = uid[idx+1:]
		}
		if unitTestCount[uid] == 1 {
			unique = append(unique, name)
		} else {
			shared = append(shared, name)
		}
	}
	unique = deduplicateStrings(unique)
	shared = deduplicateStrings(shared)
	label := "covers"
	if t.Confidence == "exact" {
		label = "exact coverage of"
	}
	const maxDisplay = 3
	if len(unique) > 0 {
		var why string
		if len(unique) <= maxDisplay {
			why = fmt.Sprintf("%s %s", label, formatUnitList(unique))
		} else {
			why = fmt.Sprintf("%s %s + %d more", label, formatUnitList(unique[:maxDisplay]), len(unique)-maxDisplay)
		}
		return why
	}
	if len(unitNames) <= maxDisplay {
		return fmt.Sprintf("%s %s", label, formatUnitList(unitNames))
	}
	return fmt.Sprintf("%s %s + %d more", label, formatUnitList(unitNames[:maxDisplay]), len(unitNames)-maxDisplay)
}

// formatUnitList renders unit names as a comma-separated inline code list.
func formatUnitList(names []string) string {
	parts := make([]string, len(names))
	for i, n := range names {
		parts[i] = "`" + n + "`"
	}
	return strings.Join(parts, ", ")
}

func deduplicateStrings(ss []string) []string {
	seen := map[string]bool{}
	var out []string
	for _, s := range ss {
		if !seen[s] {
			seen[s] = true
			out = append(out, s)
		}
	}
	return out
}

func extractUnitNames(unitIDs []string) []string {
	if len(unitIDs) == 0 {
		return nil
	}
	seen := map[string]bool{}
	var names []string
	for _, id := range unitIDs {
		name := id
		if idx := strings.LastIndex(id, ":"); idx >= 0 {
			name = id[idx+1:]
		}
		if name != "" && !seen[name] {
			seen[name] = true
			names = append(names, name)
		}
	}
	return names
}

// isEmptyPR reports whether a PR has nothing substantive to flag —
// no new findings, no AI risk section, no protection gaps. Used by
// the markdown renderer to emit a designed "all clear" callout
// instead of an awkwardly-thin comment that reads as broken.
func isEmptyPR(pr *PRAnalysis) bool {
	if pr == nil {
		return false
	}
	if len(pr.NewFindings) > 0 {
		return false
	}
	if pr.ProtectionGapCount > 0 {
		return false
	}
	if pr.AI != nil && (len(pr.AI.BlockingSignals) > 0 || len(pr.AI.UncoveredContexts) > 0) {
		return false
	}
	return true
}

// buildConfidenceHistogram renders a one-line summary of how the
// recommended test set distributes by confidence. Returns "" when
// the set is small enough that the table itself shows the same
// information clearly.
//
// Format example:
//
//	**Confidence:** 12 exact · 4 inferred · 1 weak (17 tests selected)
//
// Provides observability into how the test set was assembled,
// not just which tests were chosen.
func buildConfidenceHistogram(selections []TestSelection) string {
	if len(selections) == 0 {
		return ""
	}
	counts := map[string]int{}
	order := []string{} // first-seen order for stability
	for _, t := range selections {
		c := t.Confidence
		if c == "" {
			c = "unknown"
		}
		if _, exists := counts[c]; !exists {
			order = append(order, c)
		}
		counts[c]++
	}
	parts := make([]string, 0, len(order))
	for _, c := range order {
		parts = append(parts, fmt.Sprintf("%d %s", counts[c], c))
	}
	return fmt.Sprintf("**Confidence:** %s (%d %s selected)",
		strings.Join(parts, " · "),
		len(selections),
		pluralize(len(selections), "test", "tests"),
	)
}
